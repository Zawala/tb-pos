/* Reproduces frappe-ui/tailwind/colorPalette.js output as static v4 CSS.
   frappe-ui 0.1.278 ships a Tailwind v3 preset whose addComponents() uses
   complex selectors that Tailwind v4's plugin-compat layer rejects, so we
   can't load the preset via @config. Instead we emit the same CSS variables
   it would inject via addBase(), plus a v4 @theme that maps the semantic
   color utilities (surface/ink/outline) to those variables. */
const c = require('/home/user/Documents/github/tb-pos/node_modules/frappe-ui/tailwind/colors.json');

function resolveRef(ref) {
  const [mode, color, shade] = ref.split('/');
  if (mode === 'lightMode') return c.lightMode[color][shade];
  if (mode === 'darkMode') return c.darkMode[color][shade];
  if (mode === 'overlay') return c.overlay[color][shade];
  if (mode === 'neutral') return c.neutral[color];
  return ref;
}

const root = {};
const dark = {};
const semantic = new Set(); // "category-name" e.g. surface-white

// themed (semantic) variables
for (const cat of Object.keys(c.themedVariables.light)) {
  for (const name of Object.keys(c.themedVariables.light[cat])) {
    root[`--${cat}-${name}`] = resolveRef(c.themedVariables.light[cat][name]);
    semantic.add(`${cat}-${name}`);
  }
}
for (const cat of Object.keys(c.themedVariables.dark)) {
  for (const name of Object.keys(c.themedVariables.dark[cat])) {
    dark[`--${cat}-${name}`] = resolveRef(c.themedVariables.dark[cat][name]);
  }
}
// raw palette shade variables (referenced by some component classes)
for (const color of Object.keys(c.lightMode)) {
  for (const shade of Object.keys(c.lightMode[color])) {
    root[`--${color}-${shade}`] = c.lightMode[color][shade];
  }
}
for (const color of Object.keys(c.darkMode)) {
  for (const shade of Object.keys(c.darkMode[color])) {
    dark[`--dark-${color}-${shade}`] = c.darkMode[color][shade];
  }
}

const lines = [];
lines.push('/* GENERATED by scripts/gen-frappe-theme.cjs — do not edit by hand. */');
lines.push('/* frappe-ui design tokens for Tailwind v4 (see script header). */');
lines.push(':root {');
for (const k of Object.keys(root)) lines.push(`  ${k}: ${root[k]};`);
lines.push('}');
lines.push('');
lines.push('[data-theme="dark"] {');
for (const k of Object.keys(dark)) lines.push(`  ${k}: ${dark[k]};`);
lines.push('}');
lines.push('');
lines.push('/* Map semantic color names to v4 utilities (bg-surface-white, text-ink-gray-9, …). */');
lines.push('@theme {');
for (const s of [...semantic].sort()) lines.push(`  --color-${s}: var(--${s});`);
lines.push('}');

process.stdout.write(lines.join('\n') + '\n');
console.error(`semantic tokens: ${semantic.size}, root vars: ${Object.keys(root).length}`);
